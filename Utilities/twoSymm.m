function res = twoSymm(S,Wp)
res = [...
   2*S(4)*Wp(3,:) - 2*S(6)*Wp(2,:)
   2*S(4)*Wp(3,:) - 2*S(5)*Wp(1,:)
   2*S(6)*Wp(2,:) + 2*S(5)*Wp(1,:)
   Wp(3,:)*(S(1)-S(2)) + Wp(1,:)*S(6) - Wp(2,:)*S(5)
   Wp(1,:)*(S(2)-S(3)) + Wp(2,:)*S(4) + Wp(3,:)*S(6)
   Wp(2,:)*(S(1)-S(3)) + Wp(1,:)*S(4) - Wp(3,:)*S(5)];
end